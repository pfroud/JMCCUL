package jmccul;

/**
 *
 * @author Peter Froud
 */
public class JMCCULException extends Exception {

    public JMCCULException() {
        super();
    }

    public JMCCULException(String message) {
        super(message);
    }

    public JMCCULException(Throwable cause) {
        super(cause);
    }

    public JMCCULException(String message, Throwable cause) {
        super(message, cause);
    }

}
